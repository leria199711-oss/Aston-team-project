import java.util.List;

/**
 * Интерфейс источника данных для загрузки списка бочек.
 * Все конкретные источники (файл, случайная генерация, ручной ввод)
 * должны реализовывать этот интерфейс.
 */
public interface InputSource {

    /**
     * Загружает список бочек.
     *
     * @param size количество бочек для загрузки.
     *             Для источников с неограниченным количеством (например, файл)
     *             может интерпретироваться как максимальное число записей.
     *             Если значение <= 0, может быть выброшено исключение.
     * @return список загруженных объектов {@link Barrel}
     * @throws IllegalArgumentException если параметр size некорректен,
     *                                  или при ошибках чтения/валидации данных
     */
    List<Barrel> load(int size);
}